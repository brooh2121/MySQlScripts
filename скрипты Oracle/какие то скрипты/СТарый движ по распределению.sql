SELECT
ic.INS_COMPANY,
COUNT(e.POLICY_REC_ID) "Кол-во заключенных",
sysdate
FROM RSA_EOSAGO.EPOLICY e
JOIN RSA_EOSAGO.INS_COMPANY ic ON e.INS_COMPANY_ID = ic.INS_COMPANY_ID
WHERE e.POLICY_CREATE_DATE >= TO_DATE('02.04.2019 13:01:53','dd.mm.yyyy hh24:mi:ss')
--AND e.POLICY_CREATE_DATE < TO_DATE('16.04.2019 13:01:53','dd.mm.yyyy hh24:mi:ss')
AND e.POLICY_STATE_ID = 1 and e.IS_EGARANT = 1
AND e.INS_COMPANY_ID = 257
GROUP by ic.INS_COMPANY;

SELECT
ic.INS_COMPANY "Наименование СК",
ic.INSURER_ID "INSURER",
icd.ins_company_id "ID Компании",
icd.amount_policy_garant "Квота",
icd.distribution_code "Код региона",
icd.distribution_date "Дата распределения" 
FROM RSA_EOSAGO.ins_company_distribution icd
JOIN RSA_EOSAGO.INS_COMPANY ic ON ic.INS_COMPANY_ID = icd.ins_company_id
WHERE --distribution_date >= TRUNC(SYSDATE)
ic.INS_COMPANY_ID = 257 AND icd.DISTRIBUTION_CODE = 1
ORDER by ic.INSURER_ID;

