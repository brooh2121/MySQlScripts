SELECT
*
FROM rsa1.PERSONAL_INFO pi
WHERE lower(PI.LAST_NAME)=lower('СИДОРЕНОК')
AND LOWER(pi.FIRST_NAME)=lower('ДЕНИС') 
AND lower(PI.MIDDLE_NAMES)=lower('ВАСИЛЬЕВИЧ')
AND PI.BIRTH_DATE = TO_DATE('15.02.1979','dd.mm.yyyy');

